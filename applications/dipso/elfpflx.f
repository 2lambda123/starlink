      FUNCTION ELFPFLX(D,H,N)
      CHARACTER*40 ITXT
      COMMON/DAT1/FL,W,WS,WV,NF
      COMMON/DAT4/XV,FXV,ARP,WVF,CFLX,IXV,NXV,NPROF
      COMMON/DAT4A/ITXT
      COMMON/DAT3/ FLMAX
      DIMENSION XV(5,1000),FXV(5,1000),ARP(5),IXV(5),ITXT(5),NXV(5)
      DIMENSION FL(1000),W(1000),WVF(5),CFLX(5)
      DIMENSION FLR(400)
      IF(N.GT.5) GO TO 50
      IF(N.EQ.1) THEN
      DX=0.600561*D
      ELFPFLX=1.77245*DX*H*FLMAX/100.*WV
      ENDIF
      IF(N.EQ.2) THEN
      ELFPFLX=D*H*FLMAX/100.*WV
      ENDIF
      RETURN
   50 ELFPFLX=ARP(N-5)/CFLX(N-5)*H*FLMAX/100.*WV
      RETURN
      END
