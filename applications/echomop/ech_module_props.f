*+
*     E C H _ M O D U L E _ P R O P S
*
*    Description : Functions to determine module properties
*    Keywords : UTILITY
*
*    This routine file contains a set of functions which access
*    various bits in the 'module_properties' byte array.
*
*-
        LOGICAL FUNCTION MODULE_NEEDS( OBJECT, MODULE )
*
        INCLUDE 'ECH_MODULES.INC'
        INTEGER MODULE, OBJECT
        INTEGER IAMINT

        IAMINT = MODULE_PROPERTIES( OBJECT, MODULE )
        MODULE_NEEDS = ( IAND( 1, IAMINT ) .EQ. 1 )

        END


        LOGICAL FUNCTION MODULE_NEEDS_QUALITY( OBJECT, MODULE )
*
        INCLUDE 'ECH_MODULES.INC'
        INTEGER MODULE, OBJECT
        INTEGER IAMINT

        IAMINT = MODULE_PROPERTIES( OBJECT, MODULE )
        MODULE_NEEDS_QUALITY = ( IAND( 2, IAMINT ) .EQ. 2 )

        END


        LOGICAL FUNCTION MODULE_NEEDS_ERRORS( OBJECT, MODULE )
*
