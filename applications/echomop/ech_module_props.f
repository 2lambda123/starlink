        END


        LOGICAL FUNCTION MODULE_UPDATES( OBJECT, MODULE )
*
        INCLUDE 'ECH_MODULES.INC'
        INTEGER MODULE, OBJECT
        INTEGER IAMINT

        IAMINT = MODULE_PROPERTIES( OBJECT, MODULE )
        MODULE_UPDATES = ( IAND( 8, IAMINT ) .EQ. 8 )

        END


        SUBROUTINE MNEEDS( OBJECT, MODULE )
*
        INCLUDE 'ECH_MODULES.INC'
        INTEGER MODULE, OBJECT
        INTEGER IAMINT

        IAMINT = MODULE_PROPERTIES( OBJECT, MODULE )
        IAMINT = IOR( IAMINT, 1 )
        MODULE_PROPERTIES( OBJECT, MODULE ) = IAMINT

        END


