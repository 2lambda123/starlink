*
*
* --------------------------------------------------------------
      SUBROUTINE GK0CSL(LNTYPE)
* --------------------------------------------------------------
*
      INCLUDE '../../include/check.inc'
*
*  PURPOSE OF THE ROUTINE
*  ----------------------
*     CC81 Set CC81LineType from GKSLineType
*
*  MAINTENANCE LOG
*  ---------------
*     00/99/83  MGC  Original version stabilized
*     19/03/84  MGC  Always select line type
*     16/05/85  RMK  Removed use of hardware linestyle 5 - utility now
*                    used to produce standard RAL linestyle 5.
*
*  ARGUMENTS
*  ---------
*     INP LNTYPE   - GKSLineType [1..4]
*
      INTEGER LNTYPE
*
*  COMMON BLOCK USAGE
*  ------------------
*
      INCLUDE '../../include/gkio.par'
*
*  LOCALS
*  ------
*
      INTEGER    ILSL
      PARAMETER (ILSL=5)
      INTEGER ITYPES(4),ICSL(ILSL)
      DATA ITYPES/0,2,1,4/
*                L x  ,  5 SP
      DATA ICSL/76,0,44,53,32/
*
*  ALGORITHM
*  ---------
*     .SET LINETYPE : L<'0'+ITYPES(GKSLineType)>,5
*     .END          : <SP>
*
*  COMMENTS
*  --------
*     GKS   CC81   Description
*     1     0      solid
*     2     2      dash,dash
*     3     1      dot,dot
*     4     4      long dash, dot
*     5     -      dash, dot, dot (generated by utility)
*
* --------------------------------------------------------------------

      ICSL(2) = 48 + ITYPES(LNTYPE)
      CALL GK0CPA(KIOPB,ILSL,ICSL)
      RETURN
      END
